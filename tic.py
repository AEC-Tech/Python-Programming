import random
import time
import turtle

t = turtle.Turtle()
t.begin_fill()
t.color('blue','cyan')
t.backward(150)
t.forward(300)
t.left(90)
t.forward(300)
t.left(90)
t.forward(300)
t.left(90)
t.forward(300)
t.end_fill()
t.left(90)
t.setx(-50)
t.left(90)
t.forward(300)
t.penup()
t.setx(50)
t.sety(0)
t.pendown()
t.forward(300)
t.setx(-150)
t.sety(100)
t.right(90)
t.forward(300)
t.sety(200)
t.setx(-150)
t.forward(300)
turtle.done()